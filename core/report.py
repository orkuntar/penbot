C_DARK     = HexColor("#0d1117")
C_ACCENT   = HexColor("#e63946")
C_BLUE     = HexColor("#457b9d")
C_LIGHT_BG = HexColor("#f8f9fa")
C_BORDER   = HexColor("#dee2e6")
C_TEXT     = HexColor("#212529")
C_MUTED    = HexColor("#6c757d")
C_WHITE    = colors.white

C_CRIT     = HexColor("#dc2626")
C_CRIT_BG  = HexColor("#fef2f2")
C_HIGH     = HexColor("#ea580c")
C_HIGH_BG  = HexColor("#fff7ed")
C_MED      = HexColor("#ca8a04")
C_MED_BG   = HexColor("#fefce8")
C_LOW      = HexColor("#16a34a")
