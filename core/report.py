C_INFO     = HexColor("#2563eb")
C_INFO_BG  = HexColor("#eff6ff")

PAGE_W, PAGE_H = A4
MARGIN = 18 * mm


def sev_color(sev: str):
    return {
        "CRITICAL": (C_CRIT, C_CRIT_BG),
        "HIGH":     (C_HIGH, C_HIGH_BG),
        "MEDIUM":   (C_MED,  C_MED_BG),
        "LOW":      (C_LOW,  C_LOW_BG),
        "INFO":     (C_INFO, C_INFO_BG),
    }.get(sev.upper(), (C_MUTED, C_LIGHT_BG))


def sev_order(sev: str) -> int:
    return {"CRITICAL": 0, "HIGH": 1, "MEDIUM": 2, "LOW": 3, "INFO": 4}.get(sev.upper(), 9)


# ── Header/Footer ─────────────────────────────────────────────────────────────
class ReportCanvas(canvas.Canvas):
    def __init__(self, *args, **kwargs):
        self._info = kwargs.pop("doc_info", {})
        canvas.Canvas.__init__(self, *args, **kwargs)
        self._saved = []

    def showPage(self):
        self._saved.append(dict(self.__dict__))
        self._startPage()

    def save(self):
        total = len(self._saved)
        for state in self._saved:
            self.__dict__.update(state)
            self._draw_chrome(total)
            canvas.Canvas.showPage(self)
        canvas.Canvas.save(self)

    def _draw_chrome(self, total):
        p = self._pageNumber
        w, h = A4

        if p == 1:
            # Kapak banner
            self.setFillColor(C_DARK)
            self.rect(0, h - 68*mm, w, 68*mm, fill=1, stroke=0)
            self.setFillColor(C_ACCENT)
            self.rect(0, h - 68*mm, 3*mm, 68*mm, fill=1, stroke=0)
            self.setFillColor(C_WHITE)
            self.setFont("Helvetica-Bold", 28)
            self.drawString(MARGIN, h - 26*mm, "GREYPHANTOM")
            self.setFont("Helvetica", 11)
            self.setFillColor(HexColor("#a8dadc"))
            self.drawString(MARGIN, h - 36*mm, "Automated Pentest Report")
            self.setFont("Helvetica-Bold", 13)
            self.setFillColor(C_WHITE)
            self.drawString(MARGIN, h - 50*mm, self._info.get("target", ""))
            self.setFont("Helvetica", 9)
            self.setFillColor(HexColor("#adb5bd"))
            self.drawString(MARGIN, h - 58*mm, self._info.get("date", ""))
            self.drawRightString(w - MARGIN, h - 58*mm, "CONFIDENTIAL")
        else:
            self.setFillColor(C_DARK)
            self.rect(0, h - 13*mm, w, 13*mm, fill=1, stroke=0)
            self.setFillColor(C_ACCENT)
            self.rect(0, h - 13*mm, 3*mm, 13*mm, fill=1, stroke=0)
            self.setFont("Helvetica-Bold", 8)
            self.setFillColor(C_WHITE)
            self.drawString(MARGIN, h - 7.5*mm, "GREYPHANTOM")
            self.setFont("Helvetica", 8)
            self.setFillColor(HexColor("#a8dadc"))
            self.drawRightString(w - MARGIN, h - 7.5*mm,
                self._info.get("target", "") + "  |  CONFIDENTIAL")

        # Footer
        self.setFillColor(C_LIGHT_BG)
        self.rect(0, 0, w, 10*mm, fill=1, stroke=0)
        self.setStrokeColor(C_BORDER)
        self.setLineWidth(0.3)
        self.line(0, 10*mm, w, 10*mm)
        self.setFont("Helvetica", 7)
        self.setFillColor(C_MUTED)
        self.drawString(MARGIN, 3.5*mm, "Generated by GREYPHANTOM  •  Automated Pentest Framework  •  CONFIDENTIAL")
        self.drawRightString(w - MARGIN, 3.5*mm, f"Page {p} of {total}")


